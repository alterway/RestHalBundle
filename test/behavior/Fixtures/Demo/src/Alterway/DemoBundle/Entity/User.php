<?php

namespace Alterway\DemoBundle\Entity;

class User
{

    private $name;
    private $age;

    function __construct($name = 'Jeff')
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    public function getAge()
    {
        return $this->age;
    }

    public function setAge($age)
    {
        $this->age = $age;
        return $this;
    }

}
